export class Group {

    id: number
    name: string
    path: string
    description: string
    visibility: string
    avatar_url: string
    web_url: string
    request_access_enabled: boolean
    full_name: string
    full_path: string
    parent_id: number
    projects: Project[]
    shared_projects: Project[]

    constructor(json?: any) {
        if (!json) {
            this.projects = []
            this.shared_projects = []
            return
        }
        this.id = json.id
        this.name = json.name
        this.path = json.path
        this.description = json.description
        this.visibility = json.visibility
        this.avatar_url = json.avatar_url
        this.web_url = json.web_url
        this.request_access_enabled = json.request_access_enabled
        this.full_name = json.full_name
        this.full_path = json.full_path
        this.parent_id = json.parent_id
        this.projects = json.projects ? json.projects.map((p) => new Project(p)) : []
        this.shared_projects = json.shared_projects ? json.shared_projects.map((p) => new Project(p)) : []

    }
}

